// Get unique countries from spots
export const countries = [...new Set(sampleSpots.map(s => s.country))].sort();

// Get spots by country
export function getSpotsByCountry(countryCode) {
  return sampleSpots.filter(s => s.country === countryCode);
}

// Get top rated spots
export function getTopSpots(limit = 10) {
  return [...sampleSpots]
    .sort((a, b) => b.globalRating - a.globalRating)
    .slice(0, limit);
}

// Get recent spots
export function getRecentSpots(limit = 10) {
  return [...sampleSpots]
    .sort((a, b) => new Date(b.lastUsed) - new Date(a.lastUsed))
    .slice(0, limit);
}

export default { sampleSpots, countries, getSpotsByCountry, getTopSpots, getRecentSpots };
