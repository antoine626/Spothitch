/**
 * Get all tip categories
 */
export function getAllTipCategories() {
  return Object.keys(hitchhikingTips)
}

/**
 * Get tips by category
 */
export function getTipsByCategory(category) {
  return hitchhikingTips[category] || null
}

/**
 * Get all tips as flat array
 */
export function getAllTips() {
  return Object.values(hitchhikingTips).flatMap(cat =>
    cat.items.map(item => ({ ...item, category: cat.title, categoryEn: cat.titleEn, icon: cat.icon }))
  )
}

/**
 * Get random tip
 */
export function getRandomTip() {
  const allTips = getAllTips()
  return allTips[Math.floor(Math.random() * allTips.length)]
}

